// Express server entry point — sets up middleware, registers API routes,
// configures Vite dev server (or static serving in production), and starts listening.

import express, { type Request, Response, NextFunction } from "express";
import { registerRoutes } from "./routes";
import { serveStatic } from "./static";
import { createServer } from "http";

const app = express();
const httpServer = createServer(app);

// Extend IncomingMessage to carry raw body buffer for webhook signature verification
declare module "http" {
  interface IncomingMessage {
    rawBody: unknown;
  }
}

// JSON body parser — also stores the raw buffer on req.rawBody for cases
// where the original bytes are needed (e.g. webhook signature validation).
app.use(
  express.json({
    verify: (req, _res, buf) => {
      req.rawBody = buf;
    },
  }),
);

app.use(express.urlencoded({ extended: false }));

// Structured logger — prepends timestamp and source tag for easy filtering
export function log(message: string, source = "express") {
  const formattedTime = new Date().toLocaleTimeString("en-US", {
    hour: "numeric",
    minute: "2-digit",
    second: "2-digit",
    hour12: true,
  });

  console.log(`${formattedTime} [${source}] ${message}`);
}

// Request logging middleware — captures response JSON and logs method, path,
// status code, duration, and truncated response body for all /api routes.
app.use((req, res, next) => {
  const start = Date.now();
  const path = req.path;
  let capturedJsonResponse: Record<string, any> | undefined = undefined;

  const originalResJson = res.json;
  res.json = function (bodyJson, ...args) {
    capturedJsonResponse = bodyJson;
    return originalResJson.apply(res, [bodyJson, ...args]);
  };

  res.on("finish", () => {
    const duration = Date.now() - start;
    if (path.startsWith("/api")) {
      let logLine = `${req.method} ${path} ${res.statusCode} in ${duration}ms`;
      if (capturedJsonResponse) {
        logLine += ` :: ${JSON.stringify(capturedJsonResponse)}`;
      }

      log(logLine);
    }
  });

  next();
});

(async () => {
  // Register all API routes (also starts the background metrics collector)
  await registerRoutes(httpServer, app);

  // Global error handler — catches unhandled errors from route handlers.
  // Returns JSON error response; avoids double-sending if headers are already flushed.
  app.use((err: any, _req: Request, res: Response, next: NextFunction) => {
    const status = err.status || err.statusCode || 500;
    const message = err.message || "Internal Server Error";

    console.error("Internal Server Error:", err);

    if (res.headersSent) {
      return next(err);
    }

    return res.status(status).json({ message });
  });

  // In development, Vite serves the frontend with HMR.
  // In production, pre-built static assets are served from disk.
  // Vite setup must happen AFTER route registration so the catch-all
  // HTML fallback doesn't shadow API routes.
  if (process.env.NODE_ENV === "production") {
    serveStatic(app);
  } else {
    const { setupVite } = await import("./vite");
    await setupVite(httpServer, app);
  }

  // Listen on the PORT env var (the only non-firewalled port in Replit).
  // Serves both the API and the client on the same port.
  const port = parseInt(process.env.PORT || "5000", 10);
  httpServer.listen(
    {
      port,
      host: "0.0.0.0",
      reusePort: true,
    },
    () => {
      log(`serving on port ${port}`);
    },
  );
})();
